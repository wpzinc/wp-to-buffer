	private function delete_code_verifier() {

		return delete_option( 'wp_to_buffer_pro_code_verifier' );

	}

	/**
	 * Base64URL encode the given string.
	 *
	 * @since   6.0.0
	 *
	 * @param   string $str    String to encode.
	 * @return  string         Encoded string.
	 */
	public function base64_urlencode( $str ) {

		// Encode to Base64 string.
		$str = base64_encode( $str ); // phpcs:ignore WordPress.PHP.DiscouragedPHPFunctions

		// Convert Base64 to Base64URL by replacing “+” with “-” and “/” with “_”.
		$str = strtr( $str, '+/', '-_' );

		// Remove padding character from the end of line.
		$str = rtrim( $str, '=' );

		return $str;

	}

	/**
	 * Returns the oAuth 2 URL used to begin the oAuth process
	 *
	 * @since   3.3.3
	 *
	 * @param   string $account_id     Existing Account ID (if included, is passed in the `state` parameter and ultimately back to the WordPress site to replace an account later on).
	 * @return  string  oAuth URL
	 */
	public function get_oauth_url( $account_id = '' ) {

		// Generate and store code verifier and challenge.
		$code_verifier  = $this->generate_and_store_code_verifier();
		$code_challenge = $this->generate_code_challenge( $code_verifier );

		// Generate return URL.
		$return_url = admin_url( 'admin.php?page=' . $this->base->plugin->name . '-settings' );
		if ( ! empty( $account_id ) ) {
			$return_url = add_query_arg(
				array(
					'account_id' => $account_id,
				),
				$return_url
			);
		}

		// Build args.
		$args = array(
			'client_id'             => $this->client_id,
			'redirect_uri'          => $this->redirect_uri,
			'response_type'         => 'code',
			'scope'                 => 'posts:write posts:read ideas:read ideas:write account:read account:write offline_access',
			'state'                 => rawurlencode( $return_url ),
			'code_challenge'        => $code_challenge,
			'code_challenge_method' => 'S256',
			'prompt'                => 'consent',
		);

		// Return OAuth URL.
		return add_query_arg(
			$args,
			$this->oauth_authorize_url . 'auth'
		);

	}

